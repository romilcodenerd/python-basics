#Logical Opeators#

num1=20
num2=3

print((num1==num2) and (num1>=num2))

#Output:False

print((num1>=num2) or (num1<=num2))

#Output:True

print((num1<=num2) and (num1==num2))

#Output:False

print((num1<=num2) or (num1==num2))

#Output:False

print((num1==num2) and (num1>=num2))

#Output:False

print((not num1<=num2))

#Output:True

print((not num1>=num2))

